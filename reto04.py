from functools import reduce


def ordenes(rutinaContable):
    totalProducto = list(map(lambda x: [x[0]] + list(map(lambda y: (y[0],) + (y[1] * y[2],), x[1:])), rutinaContable))
    totalOrden = list(map(lambda x: [x[0]] + [reduce(lambda a, z: a + z, list(map(lambda y: y[1], x[1:])))], totalProducto))
    incremento = list(map(lambda x: x if x[1] >= 600000 else [x[0]] + [x[1] + 25000], totalOrden))

    print('------------------------ Inicio Registro diario ---------------------------------')
    for i in incremento:
        print("La factura {} tiene un total en pesos de {:,.2f}".format(i[0], i[1]))
    print('-------------------------- Fin Registro diario ----------------------------------')

